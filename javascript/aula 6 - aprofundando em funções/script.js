function funcao(){
    console.log('Sou uma função de uma mensagem declarativa');
}

funcao();

var funcao = function outraFuncao(){
    console.log('Sou uma mensagem de função e expressão');
}

funcao();

var funcao = function(){
    console.log('Sou uma mensagem de função e expressão');
}

funcao();

var funcao = () => {
    console.log('Sou uma arrow function');
}

funcao();